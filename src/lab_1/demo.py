def scenario_2_logical_states():
    print("Сценарий №2: Логические состояния (активен / выписан)")

    # 2.1 Создаём двух пациентов с разными характеристиками
    p_active = Patient("Анна Смирнова", 25, "Ангина", "B456")
    p_young = Patient("Дима", 17, "Насморк", "C789")

    print("\n2.1 Исходное состояние пациентов:")
    print("   Пациент 1:", p_active)
    print("   Пациент 2:", p_young)

    # 2.2 Демонстрация метода can_prescribe_medicine (зависит от возраста и активности)
    print("\n2.2 Проверка права на получение лекарств (can_prescribe_medicine):")
    print(f"   {p_active.name}: {p_active.can_prescribe_medicine()} (требуются активность и возраст ≥18)")
    print(f"   {p_young.name}: {p_young.can_prescribe_medicine()} (возраст 17)")

    # 2.3 Выписываем активного пациента
    print("\n2.3 Выписываем Анну...")
    p_active.discharge()
    print("   После выписки:", p_active)

    # 2.4 Проверяем, что состояние изменило поведение
    print("\n2.4 Поведение после выписки:")
    print(f"   Право на лекарства: {p_active.can_prescribe_medicine()}")
    print("   Пытаемся изменить диагноз выписанному пациенту:")
    try:
        p_active.update_diagnosis("Хронический тонзиллит")
    except RuntimeError as e:
        print("Ошибка:", e)

    # 2.5 Восстанавливаем пациента
    print("\n2.5 Восстанавливаем Анну...")
    p_active.reinstate()
    print("   После восстановления:", p_active)

    # 2.6 Теперь изменение диагноза работает
    print("\n2.6 Меняем диагноз после восстановления:")
    p_active.update_diagnosis("Хронический тонзиллит")
    print("Новый диагноз:", p_active.diagnosis)

    return p_active, p_young


def scenario_3_state_changes():
    print("Сценарий №3: Изменение состояния и влияние на другие операции")

    # 3.1 Создаём пациента и пробуем разные операции
    p = Patient("Сергей Петров", 20, "Перелом", "X001")

    print("\n3.1 Исходное состояние пациента:")
    print("   ", p)

    # 3.2 Изменяем диагноз (до выписки)
    print("\n3.2 Изменяем диагноз (до выписки):")
    p.update_diagnosis("Сложный перелом")
    print("   Диагноз изменён на:", p.diagnosis)

    # 3.3 Выписываем пациента
    print("\n3.3 Выписываем пациента:")
    p.discharge()
    print("   Статус после выписки:", "активен" if p.is_active else "выписан")

    # 3.4 Пытаемся выполнить операции, недоступные для выписанного
    print("\n3.4 Попытки операций с выписанным пациентом:")
    try:
        p.update_diagnosis("Новый диагноз")
    except RuntimeError as e:
        print("   - Нельзя изменить диагноз:", e)

    try:
        p.discharge()  # повторная выписка
    except RuntimeError as e:
        print("   - Нельзя выписать повторно:", e)

    # 3.5 Восстанавливаем пациента
    print("\n3.5 Восстанавливаем пациента:")
    p.reinstate()
    print("   Статус после восстановления:", "активен" if p.is_active else "выписан")

    # 3.6 Проверяем, что операции снова доступны
    print("\n3.6 После восстановления снова доступны операции:")
    p.update_diagnosis("Здоров")
    print("   Диагноз изменён на:", p.diagnosis)

    # 3.7 Демонстрация сравнения состояний через магический метод __eq__
    print("\n3.7 Сравнение объектов (__eq__):")
    p_copy = Patient("Сергей Петров", 20, "Перелом", "X001")
    print("   Сравнение с копией (тот же номер карты):", p == p_copy)
    p_other = Patient("Сергей Петров", 20, "Перелом", "X002")
    print("   Сравнение с другим номером карты:", p == p_other)


